	<a name="3"></a>
	<h2>Q. Help, I'm getting a certificate error on my windows phone!</h2>
	<p>Sadly our CA's root certificate is not shipped with windows phone operating systems, however it's simple to resolve:</p>
	<ol><li>Visit "http://www.startssl.com/certs/ca.cer" on the phone</li><li>You'll get a "Install Certificate" dialog. Click "install".</li><li>You're done. Go back to LVF</li></ol>
	<a name="4"></a>
	<h2>Q. Does LVF cover vehicles outside London?</h2>
	<p>Sadly not, currently we only receive data from TFL. We have looked for interfaces to other countdown systems but have not yet found any.</p>
	<a name="5"></a>
	<h2>Q. Why don't you include running numbers?</h2>
	<p>Unfortunately TFL do not provide this data in their feeds. Whilst it might be possible to acquire the schedules and thus work out what running number a vehicle is this is beyond anything that is worth doing.</p>
